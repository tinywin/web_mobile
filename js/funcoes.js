// Função para exibir a soma de dois valores fixos
function mostrarSoma() {
    // Valores fixos
    var valor1 = 10; // Valor 1 fixo
    var valor2 = 20; // Valor 2 fixo
    
    // Calcula a soma
    var soma = valor1 + valor2;
    
    // Exibe o resultado em um alerta
    alert('A soma dos valores é: ' + soma);
}
